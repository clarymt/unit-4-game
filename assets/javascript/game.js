$( document ).ready(function() {


var targetScore;
var loss = 0;
var win = 0;
var previous = 0;


var resetStart = function () {

    $(".crystals").empty();

    targetScore = Math.floor(Math.random() * 129 ) + 30; 

$("#result").text('Target Score: ' + targetScore);
//console.log(targetScore);

//this game will have 4 crystals and random target number
for(var i = 0; i < 4; i++){
    
    //each crystal will have random number 1-12
 
    var random = Math.floor(Math.random() * 12) + 1;
   // console.log(random);


    var crystal = $("<img>");
    
    $(crystal).addClass("crystal m-3 col-md-3 text-center img-responsive shadow-lg p-3 rounded");
    
    $(crystal).attr("src", "assets/images/crystal"+ (i + 1) +".jpg");
   
    $(crystal).val(random);
   
    $(".crystals").append(crystal);

    previous= 0; 

 
    }
}

resetStart();

$(document).on('click', ".crystal", function () {
//this declares that that the DOM has new values correspoding to any outcome generated by click


    previous = previous + parseInt(this.value);
    $("#sum").html("Your score: " + previous);
    //previous is now attached to id "sum" to update user with current accumulating values

//created corresponding outcomes for crystal sum in relation to targetScore
    if (previous > targetScore){
        loss++;

        $("#loss").html("This is going on your permanent record, deduct " + loss + " point(s) from your IQ.");

        resetStart();

    }

    else if (previous === targetScore){
        win++;
        
        $("#win").html("Congrats, you've gained " + win + " brain cell(s).");
        
        resetStart();

    }
    console.log (previous);
  
});

//each new game will generate a new random Number for each crystal
//when clicking a crystal, it should adding to the total until
//it equals target score
//if it is greater than target number it recieves +1 loss and will restart
//if it is equal, it recieves +1 win and will restart
});